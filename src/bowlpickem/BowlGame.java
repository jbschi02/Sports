package bowlpickem;

/**
 * This class represents a bowl game between two teams.
 */
public class BowlGame
{
	public final String bowlName;
	public final String team1;
	public final String team2;

	public BowlGame(String bowlName, String team1, String team2)
	{
		this.bowlName = bowlName;
		this.team1 = team1;
		this.team2 = team2;
	}
}
